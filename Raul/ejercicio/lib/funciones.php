<?php
	function suma($val1, $val2)
	{
		$resultado = $val1 + $val2;
		return $resultado;
	}
	
	function resta($val1, $val2)
	{
		$resultado = $val1 - $val2;
		return $resultado;
	}
	
	function multiplicacion($val1, $val2)
	{
		$resultado = $val1 * $val2;
		return $resultado;
	}
	
	function division($val1, $val2)
	{
		$resultado = $val1 / $val2;
		return $resultado;
	}

?>